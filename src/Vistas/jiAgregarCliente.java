    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        
        try{
        String nom = jtNombre.getText();
        String ape = jtApellido.getText();
        String ciu = jtCiudad.getText();
        String dom = jtDomicilio.getText();
        String dni = jtDni.getText();
        Long num = Long.parseLong(jtTelefono.getText());
        
        Contacto cont = new Contacto (nom,ape,dom,ciu,dni);
        
        
        
        if(r1.agregarCliente(num, cont) == null){        
            r1.agregarCliente(num, cont);
            JOptionPane.showMessageDialog(this,"Su contaco ha sido agregado"); 
        }
        else{
            JOptionPane.showMessageDialog(this,"No se logro agregar el contacto");           
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Dato incorrecto ingresado");
        }
    }//GEN-LAST:event_jbAgregarActionPerformed

